/*!
 *	@brief This API enables/disables the features of the sensor.
 */
uint16_t bma455_feature_enable(uint8_t feature, uint8_t enable, struct bma4_dev *dev)
{
	uint8_t feature_config[BMA455_FEATURE_SIZE] = {0};
	uint16_t rslt = BMA4_OK;
	uint8_t len;
	uint8_t prev_read_write_len = dev->read_write_len;
	uint8_t prev_feature_len = dev->feature_len;

	if (dev != NULL) {
		if (dev->chip_id == BMA455_CHIP_ID) {
			/* Update the length for read and write */
			update_len(&len, feature, enable, dev);
			rslt = bma4_read_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config, len, dev);
			if (rslt == BMA4_OK) {
				if (enable == TRUE) {
					/* Enables the feature */
					rslt |= feature_enable(feature, len, feature_config, dev);
				} else {
					/* Disables the feature */
					rslt |= feature_disable(feature, len, feature_config, dev);
				}
			}
			/* Restore the burst read/write and feature length */
			restore_feat_burst_rd_wr_len(prev_read_write_len, prev_feature_len, dev);
		} else {
			rslt = BMA4_E_INVALID_SENSOR;
		}
	} else {
		rslt = BMA4_E_NULL_PTR;
	}

	return rslt;
}

/*!
 *	@brief This API performs x, y and z axis remapping in the sensor.
 */
uint16_t bma455_set_remap_axes(const struct bma455_axes_remap *remap_data, struct bma4_dev *dev)
{
	uint8_t feature_config[BMA455_FEATURE_SIZE] = {0};
	uint8_t index = BMA455_AXES_REMAP_OFFSET;
	uint16_t rslt = BMA4_OK;
	uint8_t x_axis = 0;
	uint8_t x_axis_sign = 0;
	uint8_t y_axis = 0;
	uint8_t y_axis_sign = 0;
	uint8_t z_axis = 0;

	if (dev != NULL) {
		if (dev->chip_id == BMA455_CHIP_ID) {
			rslt = bma4_read_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config, BMA455_FEATURE_SIZE, dev);
			if (rslt == BMA4_OK) {
				x_axis = remap_data->x_axis & BMA455_X_AXIS_MASK;
				x_axis_sign = (remap_data->x_axis_sign << 2) & BMA455_X_AXIS_SIGN_MASK;
				y_axis = (remap_data->y_axis << 3) & BMA455_Y_AXIS_MASK;
				y_axis_sign = (remap_data->y_axis_sign << 5) & BMA455_Y_AXIS_SIGN_MASK;
				z_axis = (remap_data->z_axis << 6) & BMA455_Z_AXIS_MASK;
				feature_config[index] = x_axis | x_axis_sign | y_axis | y_axis_sign | z_axis;
				feature_config[index + 1] = remap_data->z_axis_sign & BMA455_Z_AXIS_SIGN_MASK;
				rslt |= bma4_write_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config, BMA455_FEATURE_SIZE, dev);
			}
		} else {
			rslt = BMA4_E_INVALID_SENSOR;
		}
	} else {
		rslt = BMA4_E_NULL_PTR;
	}

	return rslt;
}

