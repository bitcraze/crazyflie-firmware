{
	uint8_t feature_config[BMA455_ANYMOTION_EN_LEN + 2] = {0};
	uint8_t index = BMA455_ANY_NO_MOTION_OFFSET;
	uint16_t duration_lsb = 0;
	uint16_t duration_msb = 0;
	uint16_t duration = 0;
	uint16_t rslt = BMA4_OK;
	uint8_t prev_read_write_len = dev->read_write_len;
	uint8_t prev_feature_len = dev->feature_len;

	if (dev != NULL) {
		if (dev->chip_id == BMA455_CHIP_ID) {
			/* Update the burst read/write and feature length */
			update_feat_burst_rd_wr_len(dev);
			rslt = bma4_read_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config, BMA455_ANYMOTION_EN_LEN + 2, dev);
			if (rslt == BMA4_OK) {
				/* Assign threshold value in
				feature config array*/
				feature_config[index++] = BMA4_GET_LSB(any_motion->threshold);
				feature_config[index] = BMA4_GET_MSB(any_motion->threshold);
				/* Assign no motion selection value in
				feature config array*/
				feature_config[index++] |= (uint8_t)
					(any_motion->nomotion_sel <<
					BMA455_ANY_NO_MOTION_SEL_POS);
				/* Extract duration */
				duration_lsb = feature_config[index];
				duration_msb = feature_config[index + 1] << 8;
				duration = duration_lsb | duration_msb;
				duration = BMA4_SET_BITSLICE(duration, BMA455_ANY_NO_MOTION_DUR, any_motion->duration);
				/* Assign duration value in
				feature config array*/
				feature_config[index++] = BMA4_GET_LSB(duration);
				feature_config[index] = BMA4_GET_MSB(duration);

				/* Write any motion settings to the sensor*/
				rslt |= bma4_write_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config, BMA455_ANYMOTION_EN_LEN + 2, dev);
			}
			/* Restore the burst read/write and feature length */
			restore_feat_burst_rd_wr_len(prev_read_write_len, prev_feature_len, dev);
		} else {
			rslt = BMA4_E_INVALID_SENSOR;
		}
	} else {
		rslt = BMA4_E_NULL_PTR;
	}

	return rslt;
}

