/*!
 *	@brief This API is the entry point.
 *	Call this API before using all other APIs.
 *	This API reads the chip-id of the sensor and sets the resolution.
 */
uint16_t bma455_init(struct bma4_dev *dev)
{
	uint16_t rslt;

	rslt = bma4_init(dev);

	if (rslt == BMA4_OK) {
		if (dev->chip_id == BMA455_CHIP_ID) {
			/* Resolution of BMA455 sensor is 16 bit */
			dev->resolution = 16;
			dev->feature_len = BMA455_FEATURE_SIZE;
		} else {
			rslt |= BMA4_E_INVALID_SENSOR;
		}
	}

	return rslt;
}

/*!
 *	@brief This API is used to upload the config file to enable
 *	the features of the sensor.
 */
uint16_t bma455_write_config_file(struct bma4_dev *dev)
{
	uint16_t rslt = BMA4_OK;

	if (dev != NULL) {
		if (dev->chip_id == BMA455_CHIP_ID) {
			/* Config. stream read/write length boundary check */
			if ((dev->read_write_len >= BMA455_RD_WR_MIN_LEN) &&
				(dev->read_write_len <= BMA455_FEATURE_SIZE)) {
				/* Even or odd check */
				if ((dev->read_write_len % 2) != 0)
					dev->read_write_len = dev->read_write_len - 1;
				/*Assign stream data */
				dev->config_file_ptr = bma455_config_file;
				rslt = bma4_write_config_file(dev);
			} else {
				rslt = BMA4_E_RD_WR_LENGTH_INVALID;
			}
		} else {
			rslt = BMA4_E_INVALID_SENSOR;
		}
	} else {
		rslt = BMA4_E_NULL_PTR;
	}

	return rslt;
}

/*!
 *	@brief This API is used to get the configuration id of the sensor.
 */
uint16_t bma455_get_config_id(uint16_t *config_id, struct bma4_dev *dev)
{
	uint8_t feature_config[BMA455_FEATURE_SIZE] = {0};
	uint8_t index = BMA455_CONFIG_ID_OFFSET;
	uint16_t rslt = BMA4_OK;
	uint16_t config_id_lsb = 0;
	uint16_t config_id_msb = 0;

	if (dev != NULL) {
		if (dev->chip_id == BMA455_CHIP_ID) {
			rslt = bma4_read_regs(BMA4_FEATURE_CONFIG_ADDR, feature_config, BMA455_FEATURE_SIZE, dev);
			if (rslt == BMA4_OK) {
				config_id_lsb = (uint16_t)feature_config[index];
				config_id_msb = ((uint16_t)feature_config[index + 1]) << 8;
				*config_id = config_id_lsb | config_id_msb;
			}
		} else {
			rslt = BMA4_E_INVALID_SENSOR;
		}
	} else {
		rslt = BMA4_E_NULL_PTR;
	}

	return rslt;
}

